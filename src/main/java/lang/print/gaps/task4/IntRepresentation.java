package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
    	char c = 99;	// 'c'
    	char h = 104;	// 'h'
    	char a = 97;	// 'a'
    	char r = 114;	// 'r'
    	
    	System.out.println(c);
    	System.out.println(h);
    	System.out.println(a);
    	System.out.println(r);
    }
}
